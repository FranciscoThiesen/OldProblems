/*  Modular Multiplicative Inverse
    Using Euler's Theorem
    a^(phi(m)) = 1 (mod m)
    a^(-1) = a^(m-2) (mod m) */

long long SmallC(int n, int r, int MOD)
{
    vector< vector<long long> > C(2,vector<long long> (r+1,0));
 
    for (int i=0; i<=n; i++)
    {
        for (int k=0; k<=r && k<=i; k++)
            if (k==0 || k==i)
                C[i&1][k] = 1;
            else
                C[i&1][k] = (C[(i-1)&1][k-1] + C[(i-1)&1][k])%MOD;
    }
    return C[n&1][r];
}
 
long long Lucas(int n, int m, int q)
{
    if (n==0 && m==0) return 1;
    int ni = n % q;
    int mi = m % q;
    if (mi>ni) return 0;
    return Lucas(n/q, m/q, q) * SmallC(ni, mi, q);
}
 
long long C(int n, int r, int MOD)
{
    return Lucas(n, r, MOD);
}
 
